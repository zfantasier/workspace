function[ph,pc,pe]=sh(x,y)
    %定义在K =[-1,1]*[-1,1]上的向量函数
    ph(1) = (1-x)*(1-y)/4;
    ph(2) = (1+x)*(1-y)/4;
    ph(3) = (1-x)*(1+y)/4;
    ph(4) = (1+x)*(1+y)/4;
    %ph对x求导
    pc(1) = -(1-y)/4;
    pc(2) = (1-y)/4;
    pc(3) = -(1+y)/4;
    pc(4) = (1+y)/4;
    %ph对y的导数
    pe(1) = -(1-x)/4;
    pe(2) = -(1+x)/4;
    pe(3) = (1-x)/4;
    pe(4) = (1+x)/4;